const { createClient } = require('@supabase/supabase-js')

// Sends an email to JP whenever someone submits the promoter access form.
// Includes a one-click "Approve & send invite" button so JP doesn't have to
// log into /admin to act on a request — the button hits
// admin-approve-request with a per-row token that's valid until the request
// is processed (status flips off 'pending').
//
// POST body: { request_id, name, email, city, description }
//
// Required env:
//   SUPABASE_URL, SUPABASE_SERVICE_ROLE_KEY
//   RESEND_API_KEY
//   ADMIN_NOTIFY_EMAIL  (defaults to jp@casadecopas.com)

exports.handler = async (event) => {
  if (event.httpMethod !== 'POST') {
    return { statusCode: 405, body: JSON.stringify({ error: 'Method not allowed' }) }
  }

  try {
    const { request_id, name, email, city, description, kind } = JSON.parse(event.body || '{}')
    if (!name || !email) throw new Error('name and email required')
    const kindLabel = kind === 'artist' ? 'artist' : 'promoter'
    const kindTitle = kind === 'artist' ? 'Artist'  : 'Promoter'

    // Look up the approval_token (auto-generated by the column default in
    // migration 028). Service role to bypass RLS — this email is internal.
    let approveUrl = null
    if (request_id) {
      const supabase = createClient(
        process.env.SUPABASE_URL,
        process.env.SUPABASE_SERVICE_ROLE_KEY,
        { auth: { persistSession: false } },
      )
      const { data: req } = await supabase
        .from('promoter_requests')
        .select('approval_token')
        .eq('id', request_id)
        .maybeSingle()
      if (req?.approval_token) {
        const host = event?.headers?.host || 'grail.mx'
        const proto = host.startsWith('localhost') || host.startsWith('127.')
          ? 'http'
          : event?.headers?.['x-forwarded-proto'] || 'https'
        approveUrl = `${proto}://${host}/.netlify/functions/admin-approve-request?token=${req.approval_token}`
      }
    }

    const to = process.env.ADMIN_NOTIFY_EMAIL || 'jp@casadecopas.com'
    const subject = `GRAIL · ${name} wants ${kindLabel} access`

    const html = `
      <div style="font-family: -apple-system, system-ui, sans-serif; max-width: 480px; padding: 24px; background: #0e0e14; color: #e8e0d0; border-radius: 14px;">
        <div style="font-size: 11px; color: #dd22aa; text-transform: uppercase; letter-spacing: 0.15em; font-weight: 700; margin-bottom: 8px;">New ${kindTitle} Request</div>
        <h2 style="margin: 0 0 12px 0; color: #e8e0d0;">${escapeHtml(name)}</h2>
        <p style="margin: 0 0 6px 0;"><a href="mailto:${escapeHtml(email)}" style="color: #f07020;">${escapeHtml(email)}</a></p>
        ${city ? `<p style="margin: 0 0 18px 0; color: #8a8098;">${escapeHtml(city)}</p>` : ''}
        <div style="background: #12121a; border: 1px solid #1e1e2a; border-radius: 10px; padding: 14px 16px; color: #e8e0d0; line-height: 1.6; white-space: pre-wrap;">${escapeHtml(description || '')}</div>
        ${approveUrl ? `
        <p style="margin: 24px 0 8px 0;">
          <a href="${approveUrl}" style="display: inline-block; background: linear-gradient(135deg, #aaff00, #6abf4b); color: #000; padding: 12px 24px; border-radius: 10px; font-weight: 800; text-decoration: none; letter-spacing: 0.02em;">✓ Approve &amp; send invite</a>
        </p>
        <p style="margin: 0; color: #8a8098; font-size: 12px;">One click sends them an invite link. Open <a href="https://grail.mx/admin" style="color: #aaff00;">grail.mx/admin</a> if you want to review more first.</p>
        ` : `
        <p style="margin: 24px 0 0 0; color: #8a8098; font-size: 13px;">
          Open <a href="https://grail.mx/admin" style="color: #aaff00;">grail.mx/admin</a> to review and send an invite.
        </p>
        `}
      </div>
    `

    const res = await fetch('https://api.resend.com/emails', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${process.env.RESEND_API_KEY}`,
        'Content-Type':  'application/json',
      },
      body: JSON.stringify({
        from:     'GRAIL <hello@casadecopas.com>',
        to:       [to],
        reply_to: email,
        subject,
        html,
      }),
    })

    if (!res.ok) {
      const body = await res.text()
      throw new Error(`Resend error ${res.status}: ${body}`)
    }

    return { statusCode: 200, body: JSON.stringify({ ok: true }) }
  } catch (err) {
    console.error('notify-promoter-request error:', err)
    return { statusCode: 500, body: JSON.stringify({ error: err.message }) }
  }
}

function escapeHtml(s) {
  return String(s || '').replace(/[&<>"']/g, ch => ({ '&':'&amp;','<':'&lt;','>':'&gt;','"':'&quot;',"'":'&#39;' }[ch]))
}
